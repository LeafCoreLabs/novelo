function shuffle<T>(input: readonly T[]): T[] {
  const arr = [...input];
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j]!, arr[i]!];
  }
  return arr;
}

/**
 * Returns a freshly shuffled set of shayari snippets for the hero.
 * Called per request (the landing page is dynamic), so the order/selection
 * changes on every page load. All shayari are authored by Ravi Ranjan.
 */
export function getHeroShayari(count: number): HeroSnippet[] {
  return shuffle(SHAYARI)
    .slice(0, count)
    .map((text, i) => ({
      id: `shayari-${i}`,
      kind: "shayari" as const,
      text,
      attribution: "Ravi Ranjan",
      gradient: GRADIENTS[i % GRADIENTS.length]!,
    }));
}
